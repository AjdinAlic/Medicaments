import SwiftUI

struct LijekRow: View {
    var lijek : Lijek
    var body: some View {
        HStack {
            Rectangle()
                .fill(Color(hex: lijek.kategorija?.color ?? "#0056d6"  ))
                .frame(width: 10 , height: 40)
                VStack(alignment: .leading) {
                    Text(lijek.name.capitalized)
                    Text("\(lijek.atc ?? "NoN") - \(lijek.kategorija?.name ?? "NoN")" )
                        .font(.caption)
                    }
            
                }
    }
}

